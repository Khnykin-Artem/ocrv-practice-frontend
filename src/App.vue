const mode = ref('feed')
const allNews = ref([])
const loading = ref(true)

let cache = null

async function loadNews() {
  loading.value = true
  try {
    const data = await fetchNews()
    allNews.value = data || FALLBACK_NEWS
    cache = allNews.value
  } catch {
    if (cache) {
      allNews.value = cache
    } else {
      allNews.value = FALLBACK_NEWS
    }
  } finally {
    loading.value = false
  }
}

const visibleNews = ref([])
const predictedLayouts = ref([])

onMounted(async () => {
  await loadNews()
  if (allNews.value.length) {
    visibleNews.value = allNews.value.slice(0, VISIBLE_COUNT)
    try {
      predictedLayouts.value = await predictLayoutBatch(visibleNews.value, layoutDescriptions)
    } catch {
      predictedLayouts.value = visibleNews.value.map(() => 0)
    }
  }
})

function onCommand(action) {
  if (action === 'showNews') {
    mode.value = 'feed'
  }
}
</script>

